create sequence SEQ_BEHAVIOR_TRACKING_EVENT increment by 25;
create table BEHAVIOR_TRACKING_EVENT (
	EVENT_ID 		NUMERIC(10)		PRIMARY KEY,
	PARENT_EVENT_ID NUMERIC(10), 
	APPLICATION		VARCHAR2(32)	NOT NULL, 
	EVENT_TYPE			VARCHAR2(32)	NOT NULL, 
	EVENT_NAME			VARCHAR2(256)	NOT NULL, 
	EVENT_START			DATETIME		NOT NULL, 
	DURATION_MS		NUMERIC(10)		NOT NULL, 
	USER_ID			VARCHAR2(64), 
	SESSION_ID		VARCHAR2(64),
	ERROR			VARCHAR2(256), 
	EVENT_DATA		LONGVARCHAR
);

create sequence SEQ_BEHAVIOR_TRACKING_SUMMARY;
create table BEHAVIOR_TRACKING_SUMMARY (
	SUMMARY_ID			NUMERIC(10)		PRIMARY KEY,
	SUMMARY_DATE		DATETIME		NOT NULL,
	TYPE				VARCHAR2(32)	NOT NULL, 
	NAME				VARCHAR2(256)	NOT NULL, 
	PERIOD_START		DATETIME		NOT NULL,
	PERIOD_END			DATETIME		NOT NULL,
	EVENT_COUNT			NUMERIC(10)		NOT NULL,
	AVERAGE_MS			NUMERIC(10,2)	NOT NULL,
	MINIMUM_MS			NUMERIC(10)		NOT NULL,
	MAXIMUM_MS			NUMERIC(10)		NOT NULL,
	MEDIAN_MS			NUMERIC(10)		NOT NULL,
	STANDARD_DEVIATION	NUMERIC(10,2)	NOT NULL,
	ERROR_COUNT			NUMERIC(10)		NOT NULL
);